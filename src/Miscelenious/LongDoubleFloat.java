public class LongDoubleFloat {

	
	public static void main(String[] args) {
		
		int 	a = 9;
		long 	b = 45688L;
		
		float 	c = 2.5F;
		double 	d = 584754.98;
		
		System.out.println("This is a integer---: "+a);
		System.out.println("This is a long------: "+b);
		
		System.out.println("This is a float ----: "+c);
		System.out.println("This is a double----: "+d);
		}
		}
